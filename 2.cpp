#include <iostream>
#include <vector>
#include <string>
#include <algorithm>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    string s;
    cin >> s;

    int n = s.size();
    vector<int> z(n); // Массив для хранения значений Z-функции
    z[0] = n;         // По определению, префикс всей строки совпадает сам с собой
    
    int l = 0, r = 0; // Границы текущего Z-блока (самого правого совпадения)

    for (int i = 1; i < n; i++) {
        if (i <= r) {
            // Использование ранее посчитанных данных для оптимизации
            z[i] = min(r - i + 1, z[i - l]);
        }
        // Посимвольная проверка
        while (i + z[i] < n && s[z[i]] == s[i + z[i]]) {
            z[i]++;
        }
        // Обновление границ самого правого Z-блока
        if (i + z[i] - 1 > r) {
            l = i;
            r = i + z[i] - 1;
        }
    }

    int period = n; // По умолчанию период равен длине всей строки
    
    // Поиск минимального периода
    for (int i = 1; i < n; i++) {
        // Условие 1: длина строки делится на длину периода без остатка
        // Условие 2: Z-значение на этой позиции покрывает всю оставшуюся часть строки
        if (n % i == 0 && z[i] == n - i) {
            period = i; // Найдена минимальная длина периода
            break;      // Так как перебор идет по возрастанию, первый найденный период — кратчайший
        }
    }

    cout << period << "\n";
    return 0;
}
