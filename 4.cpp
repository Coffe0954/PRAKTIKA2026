#include <iostream>

using namespace std;

void solve() {
    int n, m, k;
    cin >> n >> m >> k;

    // Шаг 1: Выводим элементы, которые максимизируют f(i) и нейтральные элементы.
    // Идем в порядке убывания от n до m + 1. 
    // Это гарантирует, что наибольшие элементы окажутся на левых префиксах.
    for (int i = n; i > m; --i) {
        cout << i << " ";
    }
    
    // Шаг 2: Выводим «штрафные» элементы (от 1 до m) в самом конце.
    // Это минимизирует их суммарный вклад в функцию g(i), 
    // так как они попадут в минимальное число префиксов.
    for (int i = 1; i <= m; ++i) {
        cout << i << " ";
    }
    
    cout << "\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
